class Episode {
  final String title;
  final String name;
  final String duration;
  final String audioUrl;
  final String description;
  final String createdAt;
  final String image;

  Episode({
    required this.title,
    required this.name,
    required this.duration,
    required this.description,
    required this.audioUrl,
    required this.createdAt,
    required this.image,
  });
}
